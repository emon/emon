#include "capture.h"

/* simple timing routines */
static struct timeval tp1,tp2;

void timer_start(void)
{
	gettimeofday(&tp1,NULL);
}

double timer_end(void)
{
	gettimeofday(&tp2,NULL);
	return((tp2.tv_sec - tp1.tv_sec) + 
	       (tp2.tv_usec - tp1.tv_usec)*1.0e-6);
}
